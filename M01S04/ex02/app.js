var errorCode = 0;

if (errorCode === 0) {
  console.log('No error');
} else if (errorCode === 100) {
  console.log('E 100');
} else if (errorCode === 200) {
  console.log('E 200');
} else if (errorCode === 300) {
  console.log('E 300');
} else if (errorCode === 400) {
  console.log('E 400');
} else if (errorCode === 500) {
  console.log('E 500');
} else {
  console.log('E n/a');
}
